// ── Registry ─────────────────────────────────────────────────────────────────
// To add a new layout: define HID_ASCII_MAP_XX[128] and UNICODE_MAP_XX[],
// then append one entry to this array. No other code needs to change here.
static const KeyboardLayout LAYOUTS[] = {
    {"us", "English (US)", HID_ASCII_MAP_US, nullptr, 0},
    {"uk", "English (UK)", HID_ASCII_MAP_UK, UNICODE_MAP_UK,
     sizeof(UNICODE_MAP_UK) / sizeof(UNICODE_MAP_UK[0])},
    {"de", "German (QWERTZ)", HID_ASCII_MAP_DE, UNICODE_MAP_DE,
     sizeof(UNICODE_MAP_DE) / sizeof(UNICODE_MAP_DE[0])},
};
static const size_t LAYOUT_COUNT = sizeof(LAYOUTS) / sizeof(LAYOUTS[0]);

const KeyboardLayout *get_layout_by_id(const char *id) {
  if (id == nullptr) return nullptr;
  for (size_t i = 0; i < LAYOUT_COUNT; i++) {
    if (strcmp(LAYOUTS[i].id, id) == 0) return &LAYOUTS[i];
  }
  return nullptr;
}

const KeyboardLayout *default_layout() { return &LAYOUTS[0]; }

size_t layout_count() { return LAYOUT_COUNT; }

const KeyboardLayout *layout_at(size_t i) {
  return i < LAYOUT_COUNT ? &LAYOUTS[i] : nullptr;
}

